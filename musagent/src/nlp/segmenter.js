/**
 * 正向最大匹配分词
 */
function maxMatch(text, maxLen = 5) {
  const result = [];
  let i = 0;
  while (i < text.length) {
    let found = false;
    for (let len = Math.min(maxLen, text.length - i); len >= 1; len--) {
      const word = text.slice(i, i + len);
      if (CORE_DICT.has(word) || len === 1) {
        result.push(word);
        i += len;
        found = true;
        break;
      }
    }
    if (!found) i++;
  }
  return result;
}

/**
 * 中文分词 + 停用词过滤
 * @param {string} text - 输入文本
 * @returns {{ words: string[], freqMap: Record<string,number> }}
 */
export function segment(text) {
  // 移除标点、数字、英文字母
  const cleaned = text.replace(/[，。！？、；：""''（）()[\]【】\n\r\d\w\s.,!?;:'{}—…-]+/g, '|');
  const segments = cleaned.split('|').filter(Boolean);

  const allWords = [];
  for (const seg of segments) {
    allWords.push(...maxMatch(seg));
  }

  // 停用词过滤
  const filtered = allWords.filter(w => w.length >= 1 && !STOP_WORDS.has(w));

  // 词频统计
  const freqMap = {};
  for (const w of filtered) {
    freqMap[w] = (freqMap[w] || 0) + 1;
  }

  return { words: filtered, freqMap };
}

